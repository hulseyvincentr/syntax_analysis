#!/usr/bin/env python3
"""
phrase_duration_sd_resampling_supplemental_table_v1.py

Build reviewer-facing phrase-duration robustness tables and figures from the
animal x syllable pre/post SD tables already produced by the AFP lesion pipeline.

This script is meant to address three reviewer/statistics concerns using the
currently available *aggregated* CSVs:

1) Supplemental Table 1
   - one clean group-summary table, one contrast table, and one animal-level table
   - rows can be pasted into the supplemental document

2) Direct medial+lateral vs. lateral-only contrast
   - label-shuffle permutation test at the bird level
   - optional secondary contrasts versus sham

3) Bootstrap CI version of the SD-change result
   - nonparametric bird-level bootstrap confidence intervals for group medians
   - nonparametric bootstrap confidence intervals for between-group differences

Important limitation
--------------------
The CSVs this script consumes already contain pre_sd_ms and post_sd_ms for each
animal x syllable. That means the rendition-level variation has already been
collapsed. Therefore this script performs bird-level bootstrap/permutation
statistics, not the full two-level rendition-within-bird hierarchical bootstrap.
For a true rendition-level hierarchical bootstrap and held-out post-lesion
selection, you need a long-format table with one row per phrase rendition. The
script still includes table fields for "selection_basis" so you can pass in
separate pre-selected / pooled-selected / held-out-selected CSVs generated by
upstream code.

Expected input CSV columns
--------------------------
Required:
    Animal ID, Syllable, pre_sd_ms, post_sd_ms, delta_sd_ms, hit_type
Optional but useful:
    pre_n_phrases, post_n_phrases, lesion_pct, lesion_pct_avg, treatment_group

Typical usage on the current uploaded top-30% post-selected table
----------------------------------------------------------------
python phrase_duration_sd_resampling_supplemental_table_v1.py \
  --analysis "Post-selected top 30%" \
             "/Volumes/my_own_SSD/updated_AreaX_outputs/pre_post_sd_scatter_paper_format_v9/pre_post_sd_table__pre_Late_Pre__post_Post__topP70_post.csv" \
  --out-dir "/Volumes/my_own_SSD/updated_AreaX_outputs/phrase_duration_resampling_tables_v1" \
  --n-bootstrap 5000 \
  --n-permutations 10000 \
  --stat median \
  --alternative greater

Typical usage with multiple selection-validation CSVs
-----------------------------------------------------
python phrase_duration_sd_resampling_supplemental_table_v1.py \
  --analysis "Post-selected top 20%" path/to/pre_post_sd_topP80_post.csv \
  --analysis "Post-selected top 30%" path/to/pre_post_sd_topP70_post.csv \
  --analysis "Post-selected top 40%" path/to/pre_post_sd_topP60_post.csv \
  --analysis "Pooled pre+post-selected top 30%" path/to/pre_post_sd_topP70_pooled.csv \
  --analysis "Pre-selected top 30% validation" path/to/pre_post_sd_topP70_pre.csv \
  --out-dir path/to/out

Outputs
-------
Supplemental_Table_1_group_summary.csv
Supplemental_Table_1_pairwise_contrasts.csv
Supplemental_Table_1_animal_level.csv
Supplemental_Table_1_combined_for_copy_paste.csv
phrase_duration_delta_bootstrap_contrast_<label>.png/.pdf
"""

