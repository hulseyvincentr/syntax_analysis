def round_for_csv(df: pd.DataFrame) -> pd.DataFrame:
    out = df.copy()
    for col in out.columns:
        if pd.api.types.is_float_dtype(out[col]):
            out[col] = out[col].round(6)
    return out


def main() -> None:
    args = parse_args()
    out_dir = args.out_dir.expanduser().resolve()
    out_dir.mkdir(parents=True, exist_ok=True)
    rng = np.random.default_rng(args.seed)

    analyses = [AnalysisInput(label=a[0], path=Path(a[1]).expanduser().resolve()) for a in args.analysis]

    all_syllable_rows = []
    all_animal_rows = []
    for a in analyses:
        print(f"Loading {a.label}: {a.path}")
        syllable_df, animal_df = load_prepost_table(a.path, a.label)
        all_syllable_rows.append(syllable_df)
        all_animal_rows.append(animal_df)

    syllable_all = pd.concat(all_syllable_rows, ignore_index=True)
    animal_all = pd.concat(all_animal_rows, ignore_index=True)

    group_summary = make_group_summary(
        animal_all, rng=rng, B=args.n_bootstrap, P=args.n_permutations,
        stat=args.stat, alternative=args.alternative,
    )
    contrasts = make_contrast_summary(
        animal_all, rng=rng, B=args.n_bootstrap, P=args.n_permutations,
        stat=args.stat, alternative=args.alternative,
    )
    combined = combined_copy_paste_table(group_summary, contrasts)

    # Write outputs.
    paths = {
        "group_summary": out_dir / "Supplemental_Table_1_group_summary.csv",
        "pairwise_contrasts": out_dir / "Supplemental_Table_1_pairwise_contrasts.csv",
        "animal_level": out_dir / "Supplemental_Table_1_animal_level.csv",
        "combined": out_dir / "Supplemental_Table_1_combined_for_copy_paste.csv",
        "syllable_level_input_with_groups": out_dir / "Supplemental_Table_1_syllable_level_input_with_groups.csv",
    }
    round_for_csv(group_summary).to_csv(paths["group_summary"], index=False)
    round_for_csv(contrasts).to_csv(paths["pairwise_contrasts"], index=False)
    round_for_csv(animal_all).to_csv(paths["animal_level"], index=False)
    round_for_csv(combined).to_csv(paths["combined"], index=False)
    round_for_csv(syllable_all).to_csv(paths["syllable_level_input_with_groups"], index=False)

    print("\nWrote tables:")
    for p in paths.values():
        print(f"  {p}")

    primary_label = args.primary_label if args.primary_label is not None else analyses[0].label
    plot_primary_delta(
        animal_all, contrasts, primary_label, out_dir, args.prefix, args.dpi,
        stat=args.stat, show=args.show,
    )

    print("\nKey primary contrast rows:")
    key = contrasts[contrasts["contrast_type"].str.contains("primary", na=False)]
    if not key.empty:
        print(round_for_csv(key).to_string(index=False))
    print("\nDone.")


if __name__ == "__main__":
    main()
